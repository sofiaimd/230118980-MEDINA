public class SimpleAddition {
    // Function to add two numbers
    public static int add(int a, int b) {
        return a + b;
    }

    public static void main(String[] args) {
        int num1 = 5;
        int num2 = 3;
        int result = add(num1, num2);
        System.out.println("The sum is: " + result);
    }
}
